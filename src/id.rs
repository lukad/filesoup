pub fn id(length: usize, separator: &str) -> String {
    let mut rng = thread_rng();
    let mut ingredients = INGREDIENTS.to_vec();
    ingredients.shuffle(&mut rng);
    ingredients
        .iter()
        .take(length)
        .map(|s| s.to_string())
        .collect::<Vec<_>>()
        .join(separator)
}
